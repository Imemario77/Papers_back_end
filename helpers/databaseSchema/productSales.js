import mongoose from "mongoose";

const productSalesSchema = new mongoose.Schema(
  {
    salesId: {
      type: String,
      required: true,
    },
    productId: {
      type: String,
      required: true,
    },
    productName: {
      type: String,
      required: true,
    },
    quantity: {
      type: Number,
      required: true,
    },
    unitAmount: {
      type: Number,
      required: true,
    },
    totalAmount: {
      type: Number,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const ProductSales = new mongoose.model(
  "productSales",
  productSalesSchema
);
